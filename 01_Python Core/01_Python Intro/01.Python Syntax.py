# Syntax

# What is Syntax in Python?
# Syntax refers to the set of rules that defines the combinations of symbols
# that are considered to be correctly structured programs in that language.

# Execute a simple print statement in Python

print("Hello, World!") # <-- Correct Syntax


# Syntax Error Example

## print("Hello, World") <---- Error: Missing closing parenthesis

# Sum
print(5 + 3) # <-- Correct Syntax answer is 8

# Minus
