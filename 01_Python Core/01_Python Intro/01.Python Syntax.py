# Indentation

# Indentation is crucial in Python as it defines the blocks of code.

# Correct Indentation Example:

if 5 > 2:
    print("Five is greater than two!")  # <-- Correct Indentation
# Incorrect Indentation Example
# if 5 > 2:
# print("Five is greater than two!")  # <-- Error: IndentationError

# Loop with Indentation
